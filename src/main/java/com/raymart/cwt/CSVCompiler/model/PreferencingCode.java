package com.raymart.cwt.CSVCompiler.model;

public class PreferencingCode {

    private String recordName;
    private String preferencingCode;
    private String recordId;
    private String status;

    public String getRecordName() {
        return recordName;
    }

    public void setRecordName(String recordName) {
        this.recordName = recordName;
    }

    public String getPreferencingCode() {
        return preferencingCode;
    }

    public void setPreferencingCode(String preferencingCode) {
        this.preferencingCode = preferencingCode;
    }

    public String getRecordId() {
        return recordId;
    }

    public void setRecordId(String recordId) {
        this.recordId = recordId;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
