package com.raymart.cwt.CSVCompiler.model;

public class HCSProgram {

    private String hotelPortfolioName;
    private String hotelPortfolio;
    private String hcsProgramName;
    private String year;
    private String programStartDate;
    private String programEndDate;
    private String recordId;

    public String getHotelPortfolioName() {
        return hotelPortfolioName;
    }

    public void setHotelPortfolioName(String hotelPortfolioName) {
        this.hotelPortfolioName = hotelPortfolioName;
    }

    public String getHotelPortfolio() {
        return hotelPortfolio;
    }

    public void setHotelPortfolio(String hotelPortfolio) {
        this.hotelPortfolio = hotelPortfolio;
    }

    public String getHcsProgramName() {
        return hcsProgramName;
    }

    public void setHcsProgramName(String hcsProgramName) {
        this.hcsProgramName = hcsProgramName;
    }

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public String getProgramStartDate() {
        return programStartDate;
    }

    public void setProgramStartDate(String programStartDate) {
        this.programStartDate = programStartDate;
    }

    public String getProgramEndDate() {
        return programEndDate;
    }

    public void setProgramEndDate(String programEndDate) {
        this.programEndDate = programEndDate;
    }

    public String getRecordId() {
        return recordId;
    }

    public void setRecordId(String recordId) {
        this.recordId = recordId;
    }
}
