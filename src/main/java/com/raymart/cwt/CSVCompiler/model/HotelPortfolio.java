package com.raymart.cwt.CSVCompiler.model;

public class HotelPortfolio {

    private String recordName;
    private String hotelPortfolioCode;
    private String recordId;

    public String getRecordName() {
        return recordName;
    }

    public void setRecordName(String recordName) {
        this.recordName = recordName;
    }

    public String getHotelPortfolioCode() {
        return hotelPortfolioCode;
    }

    public void setHotelPortfolioCode(String hotelPortfolioCode) {
        this.hotelPortfolioCode = hotelPortfolioCode;
    }

    public String getRecordId() {
        return recordId;
    }

    public void setRecordId(String recordId) {
        this.recordId = recordId;
    }
}
